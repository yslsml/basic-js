const START_POSITION_CHARCODE = 65

class VigenereCipheringMachine {

  constructor(isDirect = true){
    this.reverseFlag = !isDirect
  }

  encrypt(message, key) {
    if (message === undefined || key === undefined) throw new Error('Lack of arguments!')

    const keyUC = key.toUpperCase()
    const keyUCDirect = keyUC.replace (/\s/g, '')

    const messageUC = message.toUpperCase()
    const messageUCDirect = messageUC.trim()

    const res = []
    
    const keyLength = keyUCDirect.length
    const msgLength = messageUCDirect.length

    for (let i=0, n=0; i< msgLength; i++, n++) {
      const messageChar = messageUCDirect[i]
      if (!~alphabet.indexOf(messageChar)){
        res.push(messageChar)
        n--
        continue
      }
      const messageCharCode = messageChar.charCodeAt(0)

      const shiftedRowIndex = keyUCDirect.charCodeAt(n % keyLength) - START_POSITION_CHARCODE
      const shiftedCharIndex = messageCharCode - START_POSITION_CHARCODE
      res.push(table[shiftedRowIndex][shiftedCharIndex])
    }
    if (this.reverseFlag === true) return res.reverse().join('')
    return res.join('')
}

  decrypt(encryptedMessage, key) {
    if (encryptedMessage === undefined || key === undefined) throw new Error('Lack of arguments!')

    const encryptedMessageUC = encryptedMessage.toUpperCase()
    const encryptedMessageUCDirect = encryptedMessageUC.trim()

    const keyUC = key.toUpperCase()
    const keyUCDirect = keyUC.replace (/\s/g, '')

    const res = []
    
    const keyLength = keyUCDirect.length
    const msgLength = encryptedMessageUCDirect.length

    for (let i=0, n=0; i< msgLength; i++, n++) {
      let encryptedMessageChar = encryptedMessageUCDirect[i]

      const rowIndex = keyUCDirect.charCodeAt(n % keyLength) - START_POSITION_CHARCODE
      const row = table[rowIndex]

      if (!~alphabet.indexOf(encryptedMessageChar)){
        res.push(encryptedMessageChar)
        n--
        continue
      }
      const targetIndex = row.indexOf(encryptedMessageChar)
      const unshiftedRow = table[0]
      const trueChar = unshiftedRow[targetIndex]

      res.push(trueChar)
    }
    if (this.reverseFlag === true) return res.reverse().join('')
    return res.join('')
  }
}

